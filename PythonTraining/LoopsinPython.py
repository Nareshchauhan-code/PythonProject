# String
name = "Naresh Chauhan"

for i in name:
    print(i)

# List

colors = ["Red", "Yellow", "Green"]

for color in colors:

    print(color)
    for char in color:
        print(char)

# Range

for k in range(10):
    print(k + 1)

for k in range(1, 11):
    print(k)

for k in range(1, 11, 2):
    print(k)
